package VO;

public class CarVO {
	
	private String bname;
	private String tname;
	private String sname;
	private int id;
	private String hk_sign;
	private String land_sign;
	
	
	public String getBname() {
		return bname;
	}
	public void setBname(String bname) {
		this.bname = bname;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getHk_sign() {
		return hk_sign;
	}
	public void setHk_sign(String hk_sign) {
		this.hk_sign = hk_sign;
	}
	public String getLand_sign() {
		return land_sign;
	}
	public void setLand_sign(String land_sign) {
		this.land_sign = land_sign;
	}
	
}
