package PO;

public class CarPO {

	String color ;
	String brand ;
	String type;
	String land_sign;
	String hk_sign;
	String store ;
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getLand_sign() {
		return land_sign;
	}
	public void setLand_sign(String land_sign) {
		this.land_sign = land_sign;
	}
	public String getHk_sign() {
		return hk_sign;
	}
	public void setHk_sign(String hk_sign) {
		this.hk_sign = hk_sign;
	}
	public String getStore() {
		return store;
	}
	public void setStore(String store) {
		this.store = store;
	}
	
}
